function scroll(val, el, timeout, step) {
    var i = 0;
    (function() {
      if (i <= val) {
        setTimeout(arguments.callee, timeout);
        document.getElementById(el).innerHTML = i;
        i = i + step;
      } else {
        document.getElementById(el).innerHTML = val;
      }
    })();
  }
